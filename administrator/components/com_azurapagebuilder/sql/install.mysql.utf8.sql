CREATE TABLE IF NOT EXISTS `#__azurapagebuilder_elements` (
  `id` int(11) unsigned NOT NULL AUTO_INCREMENT,
  `pageID` int(11) NOT NULL,
  `name` varchar(255) DEFAULT NULL,
  `type` varchar(250) DEFAULT NULL,
  `hasChild` tinyint(4) NOT NULL DEFAULT '0',
  `level` int(11) NOT NULL DEFAULT '0',
  `hasChildID` int(11) DEFAULT NULL,
  `hasParentID` int(11) DEFAULT NULL,
  `shortcode` text NOT NULL,
  `object` text,
  `content` text NOT NULL,
  `attrs` text NOT NULL,
  `ordering` int(11) NOT NULL,
  `elementOrder` int(11) NOT NULL,
  `published` tinyint(4) NOT NULL DEFAULT '1',
  `trash` tinyint(4) NOT NULL DEFAULT '0',
  `created` datetime NOT NULL DEFAULT '0000-00-00 00:00:00',
  `created_by` int(11) NOT NULL,
  `modified` datetime NOT NULL DEFAULT '0000-00-00 00:00:00',
  `modified_by` int(11) NOT NULL,
  `language` varchar(50) NOT NULL DEFAULT '*',
  PRIMARY KEY (`id`),
  KEY `idx_pageID` (`pageID`),
  KEY `idx_elementOrder` (`elementOrder`),
  KEY `idx_level` (`level`),
  KEY `idx_hasChildID` (`hasChildID`),
  KEY `idx_hasParentID` (`hasParentID`)
) ENGINE=InnoDB  DEFAULT CHARSET=utf8 AUTO_INCREMENT=1 ;