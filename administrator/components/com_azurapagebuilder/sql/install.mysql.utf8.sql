  `customJsButtonLinks` text NOT NULL,
  `customJsBottomScript` text NOT NULL,
  `jQueryLinkType` text NOT NULL,
  `noConflict` text NOT NULL,
  `metadata` text NOT NULL,
  `metadesc` text NOT NULL,
  `metakey` text NOT NULL,
  `tags` text NOT NULL,
